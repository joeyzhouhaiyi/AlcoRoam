package com.elec390coen.alcoroam.Activities.Alcohol;

public class plotReading {
    int xValue, Yvalue;

    public plotReading() {
    }

    public plotReading(int xValue, int yvalue) {
        this.xValue = xValue;
        Yvalue = yvalue;
    }

    public int getxValue() {
        return xValue;
    }

    public int getYvalue() {
        return Yvalue;
    }
}
